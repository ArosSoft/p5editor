// СОРТИРОВКА ПУЗЫРЬКОМ — ВИЗУАЛИЗАЦИЯ

let массив = [];
const количествоЭлементов = 20;

let проход = 0;
let сравнение = 0;
let идётСортировка = false;
let автоРежим = false;
let скорость = 60;   // чем меньше — тем быстрее авто-режим

function setup() {
  createCanvas(windowWidth, 520);
  textAlign(CENTER, CENTER);
  сброситьМассив();
}

function сброситьМассив() {
  массив = [];
  for (let k = 0; k < количествоЭлементов; k++) {
    массив.push(random(10, 320)); // случайные высоты столбцов
  }
  проход = 0;
  сравнение = 0;
  идётСортировка = false;
  автоРежим = false;
}

function draw() {
  background(245);

  // Заголовок
  fill(0);
  textSize(26);
  textStyle(BOLD);
  text('Сортировка пузырьком', width / 2, 40);

  textSize(15);
  textStyle(NORMAL);
  text('Сравниваем соседние элементы и поднимаем большие значения вверх', 
       width / 2, 72);

  // Рисуем столбчатую диаграмму
  const ширинаСтолбца = (width - 80) / количествоЭлементов;

  for (let k = 0; k < количествоЭлементов; k++) {
    let x = 40 + k * ширинаСтолбца;

    // Выбор цвета столбца
    if (k === сравнение || k === сравнение + 1) {
      fill('red');        // сравниваемые элементы — красные
    } 
    else if ( k >= количествоЭлементов - проход) {
      fill('green');       // уже отсортированная часть — зелёная
    } 
    else {
      fill('blue');       // обычные столбцы
    }

    rect(x, height - 90 - массив[k], ширинаСтолбца - 6, массив[k]);

    // Значение над столбцом
    fill(0);
    textSize(11);
    text(round(массив[k]), x + (ширинаСтолбца - 6) / 2, height - 100 - массив[k]);
  }

  // Информация внизу
  fill(40);
  textSize(16);
  if (идётСортировка) {
    text(`Проход: ${проход + 1} из ${количествоЭлементов - 1}   |   Сравнение: ${сравнение + 1}/${количествоЭлементов - проход - 1}`,
         width / 2, height - 45);
  } else {
    text('Сортировка завершена! Нажми R для нового массива', width / 2, height - 45);
  }

  // Подсказки по управлению
  textSize(13);
  fill(80);
  text('ПРОБЕЛ — один шаг   |   A — автосортировка (вкл/выкл)   |   R — сброс',
       width / 2, height - 20);

  // Автоматический режим
  if (автоРежим && идётСортировка && frameCount % max(2, скорость / 10) === 0) {
    выполнитьОдинШаг();
  }
}

// Один шаг сортировки пузырьком
function выполнитьОдинШаг() {
  if (!идётСортировка) {
    идётСортировка = true;
    проход = 0;
    сравнение = 0;
  }

  if (проход < количествоЭлементов - 1) {
    if (сравнение < количествоЭлементов - проход - 1) {
      if (массив[сравнение] > массив[сравнение + 1]) {
        // Меняем местами два элемента
        [массив[сравнение], массив[сравнение + 1]] = 
         [массив[сравнение + 1], массив[сравнение]];
      }
      сравнение++;
    } else {
      проход++;
      сравнение = 0;
    }
  } else {
    // Сортировка завершена
    идётСортировка = false;
    автоРежим = false;
  }
}

// ====================== УПРАВЛЕНИЕ ======================

function keyPressed() {
  if (key === ' ') {                    // Пробел — один шаг
    выполнитьОдинШаг();
  }

  if (key === 'r' || key === 'R') {     // R — сброс массива
    сброситьМассив();
  }

  if (key === 'a' || key === 'A') {     // A — вкл/выкл авторежим
    if (!идётСортировка) {
      идётСортировка = true;
      проход = 0;
      сравнение = 0;
    }
    автоРежим = !автоРежим;
  }

  // Изменение скорости авторежима
  if (key === '+' || key === '=') {
    скорость = max(10, скорость - 10);
  }
  if (key === '-') {
    скорость = min(200, скорость + 10);
  }
}

function mousePressed() {
  // Клик по области диаграммы = один шаг
  if (mouseY < height - 80) {
    выполнитьОдинШаг();
  }
}