function setup() {
  createCanvas(400, 400);
}

function draw() {
  background(135, 206, 235); // Голубое небо

  // Снеговик
  fill(255);
  noStroke();

  // Тело (три круга)
  ellipse(200, 280, 120, 120); // Низ
  ellipse(200, 190, 90, 90); // Середина
  ellipse(200, 115, 70, 70); // Голова

  // Глаза
  fill(0);
  ellipse(185, 105, 8, 8);
  ellipse(215, 105, 8, 8);

  // Нос-морковка
  fill(255, 150, 0);
  triangle(200, 115, 230, 115, 200, 130);

  // Пуговицы
  fill(0);
  ellipse(200, 170, 8, 8);
  ellipse(200, 200, 8, 8);
  ellipse(200, 230, 8, 8);

  // Шляпа
  fill(0);
  rect(170, 70, 60, 10);
  rect(180, 40, 40, 30);

  // Руки-палки
  stroke(101, 67, 33);
  strokeWeight(4);
  line(160, 170, 100, 140);
  line(240, 170, 300, 140);

  // Улыбка (просто линия)
  noFill();
  stroke(0);
  strokeWeight(2);
  arc(200, 125, 30, 15, 0, PI);

  
}
